package com.ibasco.ucgdisplay.tools.beans;

public class Comm {

    private String name;

    private int value;

    public Comm(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
